    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed

        if(jtId.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Digite um id");
        }else{
            try{
                DAO = new MensalidadeData();
                obj = DAO.pesquisar(Integer.parseInt(jtId.getText()));
                if(obj == null){
                    JOptionPane.showMessageDialog(this, "Registro não encontrado");
                }else{
                    jtPreco.setText("" + obj.getPreco());
                    jtDatapgto.setText(obj.getData_pgto());
                    jtDataVenc.setText(obj.getData_venc());
                    jtValor.setText(""+ obj.getValor());
                    jtMesRef.setText(obj.getMes_ref());
                    jcNroContrato.setEnabled(true);
                
                     for(int i=0;i<contratos.size();i++){
                         if(contratos.get(i).getNro_contrato()==obj.getContrato().getNro_contrato())
                             jcNroContrato.setSelectedIndex(i);
                     }
                    //ou jtPreco.setText(String.valueOf(obj.getPreco()));
                    jbNovo.setEnabled(false);
                    jbSalvar.setEnabled(false);
                    jbEditar.setEnabled(true);
                    jbCancelar.setEnabled(true);
                    jbPesquisar.setEnabled(false);

                }
            }catch(Exception ex){
                JOptionPane.showMessageDialog(this,"Erro ao pesquisar: " + ex.getMessage());
            }
        }    // TODO add your handling code here:
    }//GEN-LAST:event_jbPesquisarActionPerformed

    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        try {
            if(validarCampos()){
                obj = new Mensalidade();
                if(preencherObjeto()){
                    DAO = new MensalidadeData();
                    if(acao==1) { //incluir
                        if(DAO.incluir(obj)) {
                            JOptionPane.showMessageDialog(this, "Salvo com sucesso !");
                            jbCancelarActionPerformed(evt);
                        }}
                        if(acao==2){
                            if(DAO.editar(obj)){
                                JOptionPane.showMessageDialog(this, "Alterado com sucesso !");
                                jbCancelarActionPerformed(evt);
                            }}
                        }
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao salvar" +
                        ex.getMessage());
                }      // TODO add your handling code here:
    }//GEN-LAST:event_jbSalvarActionPerformed

