 private void limparCampos() {
        jtId.setText("");
        jtPreco.setText("");
        jtDatapgto.setText("");
        jtDataVenc.setText("");
       jtValor.setText("");
       jtMesRef.setText("");
       jcNroContrato.setSelectedIndex(0);
       
         
    }
    private boolean validarCampos() throws Exception{
       if(jtId.getText().equals("")){
          JOptionPane.showMessageDialog(this, "Digite o id");
          jtId.requestFocus();
          return false;
       }
         if(jtPreco.getText().equals("")){
         JOptionPane.showMessageDialog(this, "Digite o preço");
         jtPreco.setText("0,00");
         jtPreco.requestFocus();
         return false;
       }
         
          if(jtDatapgto.getText().equals("")){
         JOptionPane.showMessageDialog(this, "Digite a Data de pagamento");
         jtDatapgto.requestFocus();
         return false;
       }
       
             if(jtDataVenc.getText().equals("")){
         JOptionPane.showMessageDialog(this, "Digite a Data de vencimento");
         jtDataVenc.requestFocus();
         return false;
       }
       
        if(jtValor.getText().equals("")){
         JOptionPane.showMessageDialog(this, "Digite o Valor");
         jtValor.setText("0,00");
         jtValor.requestFocus();
         return false;
       }
       
       if( jtMesRef.getText().equals("")){
         JOptionPane.showMessageDialog(this, "Digite ao Mês de referencia");
          jtMesRef.requestFocus();
         return false;
       }
       
       if(jcNroContrato.getSelectedIndex()==0){
         JOptionPane.showMessageDialog(this, "Digite o número do contrato");
         return false;
       }

       return true;
    }
    private boolean preencherObjeto() throws Exception{
      obj.setId(Integer.parseInt(jtId.getText()));
      obj.setPreco(Float.parseFloat(jtPreco.getText().replace(",", ".").toUpperCase()));
       obj.setData_pgto(jtDatapgto.getText());
       obj.setData_venc(jtDataVenc.getText());
       obj.setValor(Float.parseFloat(jtValor.getText().replace(",", ".").toUpperCase()));
       obj.setMes_ref(jtMesRef.getText());
       obj.setContrato(contratos.
            get(jcNroContrato.getSelectedIndex()));
      return true;
    }



